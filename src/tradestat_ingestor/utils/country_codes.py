def get_country_code(country_name: str) -> str:
    """Get numeric country code from country name."""
    return COUNTRY_CODES.get(country_name.upper(), None)


def get_country_name(code: str) -> str:
    """Get country name from numeric code."""
    for name, c in COUNTRY_CODES.items():
        if c == str(code):
            return name
    return None
